package stepik.sort.pupil;

public class Pupil {
    String surName;
    String name;
    int numberOfSchool;
    double markOfMath;
    double markOfRussian;
    double markOfComputerScience;

    public Pupil(String surName, String name, int numberOfSchool, double markOfMath, double markOfRussian, double markOfComputerScience) {
        this.surName = surName;
        this.name = name;
        this.numberOfSchool = numberOfSchool;
        this.markOfMath = markOfMath;
        this.markOfRussian = markOfRussian;
        this.markOfComputerScience = markOfComputerScience;
    }
}
